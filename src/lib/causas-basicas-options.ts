// All options flat for select
export function getAllCausasBasicasOptions() {
  const all: { group: string; subgroup: string; option: string }[] = [];
  [CAUSAS_BASICAS_FATORES_PESSOAIS, CAUSAS_BASICAS_FATORES_SISTEMA].forEach(category => {
    category.groups.forEach(g => {
      g.options.forEach(o => {
        all.push({ group: category.label, subgroup: g.label, option: o });
      });
    });
  });
  return all;
}
